package JFo_Pratic3;

public class PessoasEntrevistada {
  
    private int idade;
    private String profissao;
    private String UsaPC;
    private String SO_Usado;
   

    public int getIdade() {
        return idade;
    }
    public void setIdade(int idade) {
        this.idade = idade;
    }


    public String getProfissao() {
        return profissao;
    }
    public void setProfissao(String profissao) {
        this.profissao = profissao;
    } 

    public String getUsaPC() {
        return UsaPC;
    }
    public void setUsaPC(String UsaPC) {
        this.UsaPC = UsaPC;
    }

    public String getSO_Usado() {
        return SO_Usado;
    }
    public void setSO_Usado(String sO_Usado) {
        SO_Usado = sO_Usado;
    }


   
            
}



    
   

